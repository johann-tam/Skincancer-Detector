# Imports
from django.contrib.auth.models import User
from django.db import models
from datetime import date


# Model used to store dl-models in the system, deployed and not deployed models
# Authors: Johann Tammen
class DLModel(models.Model):
    version = models.CharField(max_length=200)
    # Active field used to distinguish the deployed dl-model
    # Added by Johann Tammen & Sandra Smoler Eisenberg
    active = models.BooleanField(default=False)

    def __str__(self):
        return self.version


# Model used to store deployed models. Table is used so that we can rollback to a previous version
# Authors: Sandra Smoler Eisenberg, Johann Tammen
class DeployedModel(models.Model):
    model = models.ForeignKey(DLModel, on_delete=models.PROTECT, null=True)


# Model used to store Datasets in the system
# Authors: Johann Tammen
class DataSet(models.Model):
    name = models.CharField(max_length=200)
    dateAdded = models.DateField(auto_now_add=True)
    description = models.CharField(max_length=200, null=True)

    def __str__(self):
        return self.name


# Model to store images uploaded by end-user
# Author: Sandra Smoler Eisenberg
class UserImage(models.Model):
    title = models.CharField(max_length=200)
    image = models.ImageField(upload_to='images')
    prediction = models.IntegerField(null=True, blank=True)
    date = models.DateField(auto_now_add=True, null=True)
    user = models.ForeignKey(User, on_delete=models.CASCADE, null=True)

    def __str__(self):
        return self.title


class ModelMetrics(models.Model):
    precision = models.FloatField()
    recall = models.FloatField()
    f1 = models.FloatField()
    dl_model = models.ForeignKey(DLModel, on_delete=models.CASCADE)


